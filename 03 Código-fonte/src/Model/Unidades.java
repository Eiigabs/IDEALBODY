package Model;

public class Unidades {
	private int id_unidades;
	private String nome_unidade;
	private String endereco;

	public int getId_unidades() {
		return id_unidades;
	}

	public void setId_unidades(int id_unidades) {
		this.id_unidades = id_unidades;
	}

	public String getEndereco() {
		return endereco;
	}

	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}

	public String getNome_unidade() {
		return nome_unidade;
	}

	public void setNome_unidade(String nome_unidade) {
		this.nome_unidade = nome_unidade;
	}
	
	
}
